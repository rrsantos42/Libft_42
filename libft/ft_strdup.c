#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*dup;

	len = 0;
	i = -1;
	while (str[len])
		len++;
	dup = malloc((len + 1) * sizeof (char));
	if (!dup)
		return (NULL);
	while (str[++i])
		dup[i] = str[i];
	dup[i] = '\0';
	return (dup);
}
